#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*p;
	int		i;

	i = 0;
	p = (char *)b;
	while (len > 0)
	{
		p[i] = c;
		i++;
		len--;
	}
	return (b);
}
